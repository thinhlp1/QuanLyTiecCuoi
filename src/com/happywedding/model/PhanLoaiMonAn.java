
package com.happywedding.model;


public class PhanLoaiMonAn {
    private String maPL;
    private String tenPL;

    public PhanLoaiMonAn(String maPL, String tenPL) {
        this.maPL = maPL;
        this.tenPL = tenPL;
    }

    public String getMaPL() {
        return maPL;
    }

    public void setMaPL(String maPL) {
        this.maPL = maPL;
    }

    public String getTenPL() {
        return tenPL;
    }

    public void setTenPL(String tenPL) {
        this.tenPL = tenPL;
    }


}
