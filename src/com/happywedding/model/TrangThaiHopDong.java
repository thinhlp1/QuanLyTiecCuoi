
package com.model;


public class TrangThaiHopDong {
     private String maTT;
     private String tenTT;

    public TrangThaiHopDong(String maTT, String tenTT) {
        this.maTT = maTT;
        this.tenTT = tenTT;
    }

    public String getMaTT() {
        return maTT;
    }

    public void setMaTT(String maTT) {
        this.maTT = maTT;
    }

    public String getTenTT() {
        return tenTT;
    }

    public void setTenTT(String tenTT) {
        this.tenTT = tenTT;
    }

    
}
